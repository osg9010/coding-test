public class APlusB_3_10950 {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        int a = in.nextInt();

        for (int i = 0; i < a; i++) {
            int i1 = in.nextInt();
            int i2 = in.nextInt();
            System.out.println(i1+i2);
        }

    }
}
